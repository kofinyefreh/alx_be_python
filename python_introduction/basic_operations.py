# Define variables
number1 = 10
number2 = 5

# sum
sumNo = number1 + number2
print (f'Addition of {number1} and {number2} is {sumNo}')

# difference
difference = number2 - number1
print (f'Subtraction of {number2} and {number1} is {difference}')

# multiplication
product = number1 * number2
print (f'Multiplication of {number1} and {number2} is {product}')

